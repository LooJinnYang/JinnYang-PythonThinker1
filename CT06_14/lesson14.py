import turtle
window = turtle.Screen()
window.setup(width = 600, height = 400)
t = turtle.Turtle()
t.shape("turtle")
t.fillcolor("green")
t.pendown()
for i in range(5):
    t.forward(200)
    t.right(179)
t.penup()
window.mainloop()